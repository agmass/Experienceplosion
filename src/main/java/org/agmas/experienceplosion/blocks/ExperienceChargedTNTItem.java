public class ExperienceChargedTNTItem extends PolymerBlockItem {

    PolymerModelData modelData = PolymerResourcePackUtils.requestModel(Items.NOTE_BLOCK, Identifier.of("experienceplosion","item/tnt"));

    public ExperienceChargedTNTItem(Block block, Settings settings) {
        super(block, settings, Items.NOTE_BLOCK);
    }

    @Override
    public ItemStack getPolymerItemStack(ItemStack itemStack, TooltipType tooltipType, RegistryWrapper.WrapperLookup lookup, @Nullable ServerPlayerEntity player) {
        ItemStack stack = super.getPolymerItemStack(itemStack,tooltipType,lookup,player);
        stack.set(DataComponentTypes.CUSTOM_MODEL_DATA, modelData.asComponent());
        return stack;
    }


}
